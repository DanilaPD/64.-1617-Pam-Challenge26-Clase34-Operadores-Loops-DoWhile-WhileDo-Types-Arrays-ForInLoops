//* Do-While:

let a = 5;

while (a <= 19) {

    console.log(a);

    a++;
}

//* While-Do:

let e = 5;

do {

    e++;
    console.log(e);

} while (e <= 19);

//!

//* Do-While:

let b = 100;

while (b >= 80) {

    console.log(b);

    b--;
}

//* While-Do:

let f = 100;

do {

    f--;
    console.log(f);

} while (f >= 80);

